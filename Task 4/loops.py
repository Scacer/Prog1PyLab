# Create a for loop that iterates from 0 to 10 and prints out the index

for i in range(0, 10):
    print(i)

# Create a foor loop that prints out odd numbers between 0 and 20
for i in range(0, 20):
    if (i % 2) == 1:
        print(i)

# Create a for loop that prints out 0 to 10 in reverse
